#include "fillit.h"

void		ft_move_up(t_tet *t, int *ok)
{
	int		i;
	int		j;

	*ok = 1;
	i = 1;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			t->btet[i - 1][j] = t->btet[i][j];
			j++;
		}
		i++;
	}
	i = 0;
	while (i < 4)
	{
		t->btet[3][i] = '.';
		i++;
	}
}

#include <unistd.h>
#include "fillit.h"

int			ft_check_buf(char *f_ch)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (i < 19)
	{
		if (f_ch[i] != '.' && f_ch[i] != '#' && f_ch[i] != '\n')
			return (-1);
		if ((i + 1) % 5 == 0)
			if (f_ch[i] != '\n')
				return (-2);
		if (f_ch[i] == '#')
			j++;
		i++;
	}
	if (j != 4)
		return (-3);
	return (0);
}

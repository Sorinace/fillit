#include "fillit.h"

void		ft_write(t_sq *f, t_tet t, t_point p, int min)
{
	int		a;
	int		b;

	b = 0;
	while (b < t.y)
	{
		a = 0;
		while (a < t.x)
		{
			if (t.btet[b][a] != '.')
				f->s[p.y + b][p.x + a] = t.btet[b][a];
			a++;
		}
		b++;
	}
	f->d = min;
	f->tot += 1;
}

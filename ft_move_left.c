#include "fillit.h"

void		ft_move_left(t_tet *t, int *ok)
{
	int		i;
	int		j;

	*ok = 1;
	i = 0;
	while (i < 4)
	{
		j = 1;
		while (j < 4)
		{
			t->btet[i][j - 1] = t->btet[i][j];
			j++;
		}
		i++;
	}
	i = 0;
	while (i < 4)
	{
		t->btet[i][3] = '.';
		i++;
	}
}

#include "fillit.h"

void		ft_clone(t_sq *f, t_sq *sq, int n)
{
	int		i;
	int		j;

	i = 0;
	while (i < n)
	{
		j = 0;
		while (j < n)
		{
			f->s[i][j] = sq->s[i][j];
			j++;
		}
		i++;
	}
	f->d = sq->d;
	f->tot = sq->tot;
}
